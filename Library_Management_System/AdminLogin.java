     public boolean validateLogin(){
         
         
        String name = txt_username.getText();
        String password= txt_password.getText();
        
        if(name.equals("")){
            JOptionPane.showMessageDialog(this, "pleare eneter username");
            return false;
        }
        
          
        if(password.equals("")){
            JOptionPane.showMessageDialog(this, "pleare eneter password");
            return false;
        }
        return true;
     }
     
     
