     public void login(){
         
         String username = txt_username.getText();
        String password= txt_password.getText();
        
         try {
                  Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management_system","root","gupta22@");
           PreparedStatement pst = con.prepareStatement("select * from adminlogin where username = ? and password =?");
               pst.setString(1, username);
               pst.setString(2, password);
               
               ResultSet rs = pst.executeQuery();
               if(rs.next()){
                   
                   JOptionPane.showMessageDialog(this, "Login Successful");
                   HomePage home = new HomePage();
                   home.setVisible(true);
                   this.dispose();
               }
               else{
                   JOptionPane.showMessageDialog(this, "incorrect username and password");
               
               }
               
               
         } catch (Exception e) {
             e.printStackTrace();
         }
     
     
     
     }
     
   
   
  


